package com.lab;

import java.util.Arrays;

public class LetterRecognitionMain {
    public static void main(String[] args) {
        // Максимальна кількість епох для навчання
        final int maxEpochs = 100;

        // Кількість ознак (входів штучного нейрона) відповідно до методички
        int n = 6;

        // Створюємо новий порожній персептрон для розпізнавання літери "Ц"
        Perceptron letterClassifier = new Perceptron(n);

        /*
         * Вхідні дані (Вектори ознак для літер згідно з Таблицею/Поясненням):
         * x1 - наявність лівої вертикальної лінії
         * x2 - наявність правої вертикальної лінії
         * x3 - наявність нижньої горизонтальної лінії
         * x4 - наявність хвостика праворуч знизу (ключова ознака для "Ц" та "Щ")
         * x5 - наявність середньої горизонтальної чи вертикальної лінії (напр. для Ш, Щ)
         * x6 - наявність верхньої горизонтальної лінії (напр. для Г, Д)
         */
        double[][] trainX = {
                {1, 1, 1, 1, 0, 0}, // Літера "Ц"
                {1, 1, 1, 0, 1, 0}, // Літера "Ш"
                {1, 1, 1, 0, 0, 1}, // Літера "Д"
                {1, 1, 1, 1, 1, 0}, // Літера "Щ"
                {1, 0, 0, 0, 0, 1}  // Літера "Г"
        };

        // Вектор цільових значень (Target): 1 — це "Ц" (шуканий клас), 0 — всі інші літери
        int[] trainT = {
                1, // Ц (Шукана літера)
                0, // Ш
                0, // Д
                0, // Щ
                0  // Г
        };

        // Масив назв літер для красивого виведення результатів у консоль
        String[] letterNames = {"Ц", "Ш", "Д", "Щ", "Г"};

        System.out.println("=================================================================");
        System.out.println("НАВЧАННЯ ПЕРСЕПТРОНА ДЛЯ РОЗПІЗНАВАННЯ ЛІТЕРИ \"Ц\"");
        System.out.println("=================================================================");

        // Запускаємо стандартний цикл навчання з Лабораторної роботи №3
        letterClassifier.train(trainX, trainT, maxEpochs);

        System.out.println("\n=================================================================");
        System.out.println("РЕЗУЛЬТАТИ ТЕСТУВАННЯ СИСТЕМИ (PREDICT)");
        System.out.println("=================================================================");

        // Перевіряємо, як персептрон навчився розрізняти літери
        for (int i = 0; i < trainX.length; i++) {
            int result = letterClassifier.predict(trainX[i]);
            String status = (result == trainT[i]) ? "УСПІШНО" : "ПОМИЛКА";

            System.out.println("Літера \"" + letterNames[i] + "\" " + Arrays.toString(trainX[i]) +
                    " -> Розпізнано як \"Ц\": " + (result == 1 ? "ТАК" : "НІ") +
                    " | Статус: " + status);
        }
    }
}